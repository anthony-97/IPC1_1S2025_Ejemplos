    public void llenarCarrosListos(Cliente c) {
        this.carrosListosLabel.setText("");
        for(OrdenTrabajo o: c.getListaOrdenes()) {
            if(o.getEstado() == 3) {
                this.carrosListosLabel.setText(this.carrosListosLabel.getText() + " " + o.getPlaca());
            }
        }
    }
    
    
