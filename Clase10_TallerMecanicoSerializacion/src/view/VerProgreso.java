    private void addOrdenBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrdenBotonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.tablaOrdenes.getModel();
        modelo.setRowCount(0);
       
        String auto = (String) this.listaAutomoviles.getSelectedItem(); //Se obtiene el item seleccionado
        String serv = (String) this.listaServicios.getSelectedItem(); //Se obtiene el item seleccionado
        String carro = auto.split(" - ")[0];
        String servicio = serv.split(" - ")[0];
        //Se manda a crear la orden y se obtiene al cliente para cargar sus ordenes
        Cliente cliente = this.getControlador().crearOrdenTrabajo(carro, servicio); 
        
        Vector<OrdenTrabajo> ordenes = cliente.obtenerOrdenesSinAsignar();

        Vector<String[]> ordenesRow = new Vector<String[]>();
        for(OrdenTrabajo orden: ordenes) {
            ordenesRow.add(new String[]{ orden.getId(), orden.getPlaca(), orden.getServicio() } );
        }
        
        //Llenar la tabla
        for (String[] orden : ordenesRow) {
            modelo.addRow(orden);
        }
    }//GEN-LAST:event_addOrdenBotonActionPerformed

