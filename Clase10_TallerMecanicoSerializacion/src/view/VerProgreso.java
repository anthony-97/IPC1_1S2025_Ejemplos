    public void llenarListas() {
        Vector<Automovil> autos = this.getControlador().obtenerAutos(this.getControlador().getUsuarioActual());
        this.listaAutomoviles.removeAllItems(); //Limpio la lista desplegable
        for(Automovil auto: autos) {
            this.listaAutomoviles.addItem(auto.getPlaca()+" - " + auto.getMarca() + " - " + auto.getModelo());
        }
        Vector<Servicio> servicios = this.getControlador().getModelo().getListaServicios();
        this.listaServicios.removeAllItems(); //Limpio la 2 lista desplegable
        for(Servicio servicio: servicios) {
            this.listaServicios.addItem(servicio.getId()+" - " + servicio.getNombre());
        }
    }
    
    public void vaciarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) this.tablaOrdenes.getModel();
        modelo.setRowCount(0);
    }

